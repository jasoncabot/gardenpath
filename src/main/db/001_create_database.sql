CREATE TABLE IF NOT EXISTS games (
	id INTEGER NOT NULL AUTO_INCREMENT,
	state VARCHAR(255) NOT NULL DEFAULT 'UNKNOWN',
	last_move_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
	name VARCHAR(255) NULL,
	hashed_password VARCHAR(255) NULL,
	is_player_one_turn TINYINT(1) NOT NULL DEFAULT 0,
	p1_id VARCHAR(255) NULL,
	p1_name VARCHAR(255) NULL,
	p1_position INTEGER NOT NULL DEFAULT 0,
	p1_f1 INTEGER NOT NULL DEFAULT 0,
	p1_f2 INTEGER NOT NULL DEFAULT 0,
	p1_f3 INTEGER NOT NULL DEFAULT 0,
	p1_f4 INTEGER NOT NULL DEFAULT 0,
	p1_f5 INTEGER NOT NULL DEFAULT 0,
	p1_f6 INTEGER NOT NULL DEFAULT 0,
	p1_f7 INTEGER NOT NULL DEFAULT 0,
	p1_f8 INTEGER NOT NULL DEFAULT 0,
	p1_f9 INTEGER NOT NULL DEFAULT 0,
	p1_f10 INTEGER NOT NULL DEFAULT 0,
	p2_id VARCHAR(255) NULL,
	p2_name VARCHAR(255) NULL,
	p2_position INTEGER NOT NULL DEFAULT 0,
	p2_f1 INTEGER NOT NULL DEFAULT 0,
	p2_f2 INTEGER NOT NULL DEFAULT 0,
	p2_f3 INTEGER NOT NULL DEFAULT 0,
	p2_f4 INTEGER NOT NULL DEFAULT 0,
	p2_f5 INTEGER NOT NULL DEFAULT 0,
	p2_f6 INTEGER NOT NULL DEFAULT 0,
	p2_f7 INTEGER NOT NULL DEFAULT 0,
	p2_f8 INTEGER NOT NULL DEFAULT 0,
	p2_f9 INTEGER NOT NULL DEFAULT 0,
	p2_f10 INTEGER NOT NULL DEFAULT 0,
	PRIMARY KEY(id)
);